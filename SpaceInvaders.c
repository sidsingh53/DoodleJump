void	PortF_Init(){
	uint8_t nothing = 0;
	SYSCTL_RCGCGPIO_R |= 0x30;
	nothing++;
	nothing++;
	nothing++;
	nothing++;
	GPIO_PORTF_LOCK_R = 0x4C4F434B; //unlock
	//GPIO_PORTE_CR_R |= 0x1F;
	GPIO_PORTE_DIR_R	&= 0xFD;
	GPIO_PORTF_PUR_R	|= 0x11;
	GPIO_PORTE_DEN_R	|= 0x02;
}

void PortE_Init(){
	SYSCTL_RCGCGPIO_R |= 0x11;
	GPIO_PORTE_DIR_R &= ~0x11;
	GPIO_PORTE_AFSEL_R &= ~0x10;
	GPIO_PORTE_DEN_R |= 0x11;
	GPIO_PORTE_AMSEL_R &= ~0x10;
	GPIO_PORTE_PCTL_R &= ~0x000F0000;
	GPIO_PORTE_IS_R &= ~0x10;
	GPIO_PORTE_IBE_R &= ~0x10;
	GPIO_PORTE_IEV_R |= 0x10;
	GPIO_PORTE_ICR_R = 0x10;
	GPIO_PORTE_IM_R |= 0x10;
	NVIC_PRI1_R = (NVIC_PRI1_R & 0xFFFFFF00) | 0x00000040;
	NVIC_EN0_R = 0x00000014;
	EnableInterrupts();
}
	

